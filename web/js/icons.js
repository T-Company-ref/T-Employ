/**
 * @param {string} name
 * @param {{ size?: number, className?: string, title?: string, label?: string }} [opts]
 */
export function icon(name, { size = 16, className = "", title = "", label = "" } = {}) {
  const body = ICONS[name];
  if (!body) return "";
  const cls = ["ui-icon", className].filter(Boolean).join(" ");
  const aria = label || title
    ? `role="img" aria-label="${escAttr(label || title)}"`
    : `aria-hidden="true"`;
  const titleEl = title ? `<title>${escAttr(title)}</title>` : "";
  return `<svg class="${cls}" width="${size}" height="${size}" viewBox="0 0 24 24" ${ATTR} ${aria}>${titleEl}${body}</svg>`;
}

function escAttr(s) {
  return String(s ?? "")
    .replace(/&/g, "&amp;")
    .replace(/"/g, "&quot;")
    .replace(/</g, "&lt;");
}

